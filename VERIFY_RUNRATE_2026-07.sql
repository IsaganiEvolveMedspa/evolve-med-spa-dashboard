wd_cash AS (
    SELECT location,
           SUM(CASE WHEN CAST(dt AS DATE) <= @e_cash THEN 1 ELSE 0 END) AS elapsed_wd,
           COUNT(*)                                                     AS total_wd
    FROM open_days GROUP BY location
),
wd_sale AS (
    SELECT location,
           SUM(CASE WHEN CAST(dt AS DATE) <= @e_sale THEN 1 ELSE 0 END) AS elapsed_wd,
           COUNT(*)                                                     AS total_wd
    FROM open_days GROUP BY location
),
cash AS (
    SELECT center_name, SUM(sales_collected_exc_tax) AS mtd
    FROM dbo.BRONZE_ZENOTI_CASH_COLLECTIONS
    WHERE CAST(payment_date AS DATE) BETWEEN @s AND @e_cash
      AND ((',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,card,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,cash,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,check,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,custom - %')
    GROUP BY center_name
),
recog AS (
    SELECT center_name, SUM(sales_inc_tax) AS mtd
    FROM dbo.BRONZE_ZENOTI_SALES_ACCRUAL
    WHERE CAST(sale_date AS DATE) BETWEEN @s AND @e_sale
    GROUP BY center_name
),
cash_rr AS (
    SELECT c.center_name AS location,
           CAST(c.mtd AS DECIMAL(18,2)) AS mtd_val,
           w.elapsed_wd, w.total_wd,
           CAST(c.mtd / NULLIF(w.elapsed_wd,0) * w.total_wd AS DECIMAL(18,2)) AS run_rate
    FROM cash c LEFT JOIN wd_cash w ON w.location = c.center_name
),
recog_rr AS (
    SELECT r.center_name AS location,
           CAST(r.mtd AS DECIMAL(18,2)) AS mtd_val,
           w.elapsed_wd, w.total_wd,
           CAST(r.mtd / NULLIF(w.elapsed_wd,0) * w.total_wd AS DECIMAL(18,2)) AS run_rate
    FROM recog r LEFT JOIN wd_sale w ON w.location = r.center_name
),
results AS (
    SELECT 'CASH' AS metric, @e_cash AS elapsed_through, location, mtd_val AS mtd, elapsed_wd, total_wd, run_rate,
           CASE WHEN location = 'CHAIN TOTAL' THEN 1 ELSE 0 END AS is_total
    FROM cash_rr
    UNION ALL
    SELECT 'CASH', @e_cash, 'CHAIN TOTAL', CAST(SUM(mtd_val) AS DECIMAL(18,2)), NULL, NULL, CAST(SUM(run_rate) AS DECIMAL(18,2)), 1 FROM cash_rr
    UNION ALL
    SELECT 'RECOGNIZED', @e_sale, location, mtd_val, elapsed_wd, total_wd, run_rate,
           CASE WHEN location = 'CHAIN TOTAL' THEN 1 ELSE 0 END
    FROM recog_rr
    UNION ALL
    SELECT 'RECOGNIZED', @e_sale, 'CHAIN TOTAL', CAST(SUM(mtd_val) AS DECIMAL(18,2)), NULL, NULL, CAST(SUM(run_rate) AS DECIMAL(18,2)), 1 FROM recog_rr
)
SELECT metric, elapsed_through, location, mtd, elapsed_wd, total_wd, run_rate
FROM results
ORDER BY metric, is_total, location;
