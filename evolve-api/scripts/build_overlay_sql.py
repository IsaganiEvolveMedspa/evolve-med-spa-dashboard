def overlay_name(bronze: str) -> str:
    return bronze.replace("dbo.BRONZE_", "dbo.OVERLAY_")


def view_name(bronze: str) -> str:
    return bronze.replace("dbo.BRONZE_", "dbo.V_")


def read_rows(path):
    rows = list(csv.reader(open(path, encoding="utf-8-sig")))
    hi = next((i for i, r in enumerate(rows) if sum(1 for c in r if c.strip()) > 3), 0)
    hdr = rows[hi]
    idx = {h: j for j, h in enumerate(hdr)}
    data = [r for r in rows[hi + 1:] if any(c.strip() for c in r)]
    return idx, data


def sql_val(v):
    v = (v or "").strip()
    if v == "":
        return "NULL"
    return "'" + v.replace("'", "''") + "'"


def main():
    out = ["-- AUTO-GENERATED by scripts/build_overlay_sql.py — Zenoti overlay setup.",
           "-- Run once to create OVERLAY_* staging tables + V_* views (gap-fill, self-disabling).",
           "-- Remove: DROP the V_* views and OVERLAY_* tables (or set OVERLAY_ENABLED=false).",
           ""]
    for t in TABLES:
        bronze = t["bronze"]; ov = overlay_name(bronze); vw = view_name(bronze); dcol = t["date"]
        idx, data = read_rows(os.path.join(DATA, t["csv"]))
        cols = [c for c in t["map"] if t["map"][c] in idx]     # bronze cols we can populate
        collist = ", ".join(cols)
        out.append(f"-- ===== {bronze} ({len(data)} overlay rows) =====")
        out.append(f"IF OBJECT_ID('{ov}') IS NULL SELECT TOP 0 * INTO {ov} FROM {bronze};")
        out.append(f"TRUNCATE TABLE {ov};")
        # batched INSERTs (<=500 rows per statement)
        B = 500
        for i in range(0, len(data), B):
            chunk = data[i:i + B]
            out.append(f"INSERT INTO {ov} ({collist}) VALUES")
            vals = []
            for r in chunk:
                vals.append("(" + ", ".join(sql_val(r[idx[t['map'][c]]]) for c in cols) + ")")
            out.append(",\n".join(vals) + ";")
        # gap-fill view: overlay only contributes dates the bronze doesn't have yet
        out.append(
            f"GO\nCREATE OR ALTER VIEW {vw} AS "
            f"SELECT * FROM {bronze} "
            f"UNION ALL "
            f"SELECT * FROM {ov} ov "
            f"WHERE CAST(ov.{dcol} AS DATE) > (SELECT MAX(CAST({dcol} AS DATE)) FROM {bronze});\nGO")
        out.append("")
    with open(OUT, "w", encoding="utf-8") as f:
        f.write("\n".join(out))
    print("SAVED", OUT)


if __name__ == "__main__":
    main()
