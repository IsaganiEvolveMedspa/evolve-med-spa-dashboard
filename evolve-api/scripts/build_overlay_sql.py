"""
Build data/overlay/overlay_setup.sql from the Zenoti CSV exports in data/overlay/.

Purpose: fill the gap when the bronze warehouse tables lag the live site (e.g. the
latest day's rows not yet ingested). The generated SQL:

  1. Clones each bronze table's schema into an OVERLAY_* staging table
     (SELECT TOP 0 * INTO ... — no need to know the full column list).
  2. Loads the CSV rows into the OVERLAY table (mapping report column names ->
     bronze column names; unmapped bronze columns stay NULL).
  3. Creates a V_* view = bronze UNION ALL overlay-for-dates-the-bronze-lacks
     (gap-fill: WHERE overlay.<date> > MAX(bronze.<date>) — so once the pipeline
     catches up, the overlay contributes nothing = no double-count).

The app then points FULL_* at the V_* views when OVERLAY_ENABLED=true (config.py).
Views are drop-in table replacements, so NO query changes are needed.

Re-run after dropping newer CSV exports in data/overlay/. To remove entirely:
drop the V_* views and OVERLAY_* tables (or set OVERLAY_ENABLED=false).
"""
