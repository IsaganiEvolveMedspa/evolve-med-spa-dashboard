wd AS (   -- elapsed (open days through latest data day) and total (whole month) per location
    SELECT location,
           SUM(CASE WHEN CAST(dt AS DATE) <= @e THEN 1 ELSE 0 END) AS elapsed_wd,
           COUNT(*)                                                AS total_wd
    FROM open_days
    GROUP BY location
),
cash AS (
    SELECT center_name, SUM(sales_collected_exc_tax) AS mtd_cash
    FROM dbo.BRONZE_ZENOTI_CASH_COLLECTIONS
    WHERE CAST(payment_date AS DATE) BETWEEN @s AND @e
      AND ((',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,card,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,cash,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,check,%'
        OR  (',' + REPLACE(LOWER(LTRIM(RTRIM(payment_type))), ', ', ',') + ',') LIKE '%,custom - %')
    GROUP BY center_name
),
per_loc AS (
    SELECT c.center_name AS location,
           CAST(c.mtd_cash AS DECIMAL(18,2))                                   AS mtd_cash,
           wd.elapsed_wd, wd.total_wd,
           CAST(c.mtd_cash / NULLIF(wd.elapsed_wd,0) * wd.total_wd AS DECIMAL(18,2)) AS run_rate
    FROM cash c LEFT JOIN wd ON wd.location = c.center_name
)
